    private void bt_volverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_volverActionPerformed
        javax.swing.JDesktopPane dp = getDesktopPane();
        if (dp == null) return;
        for (javax.swing.JInternalFrame f : dp.getAllFrames()) {
            if (f instanceof FRMInventario) {
                f.setVisible(true);
                break;
            }
        }
        this.dispose();
    }//GEN-LAST:event_bt_volverActionPerformed

    private void bt_editar_InventarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editar_InventarioActionPerformed
        Ambiente amb = new Ambiente().buscarPorId(idAmbiente);
        if (amb == null) {
            JOptionPane.showMessageDialog(this, "No se encontró el ambiente");
            return;
        }
        javax.swing.JDesktopPane dp = getDesktopPane();
        if (dp == null) return;
        for (javax.swing.JInternalFrame f : dp.getAllFrames()) {
            if (f instanceof FRMInventario) {
                FRMNEditarInventario nuevoAmb = new FRMEditarInventario((FRMInventario) f);
                nuevoAmb.cargarAmbiente(amb);
                dp.add(nuevoAmb);
                nuevoAmb.setVisible(true);
                setVisible(false);
                break;
            }
        }
    }//GEN-LAST:event_bt_editar_InventarioActionPerformed

    

