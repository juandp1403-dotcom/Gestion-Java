    public void cargarInformacionInventario(int idAmbiente) {
        this.idAmbiente = idAmbiente;

    try {
        PreparedStatement ps = conexion.prepareStatement(
            "SELECT nombre, tipo, ubicacion FROM ambiente WHERE id = ?");

        ps.setInt(1, idAmbiente);

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            lb_titulo.setText(rs.getString("nombre"));
            txt_informacion.setText(
                "Tipo: " + rs.getString("tipo") +
                " | Ubicación: " + rs.getString("ubicacion")
            );
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }

    cargarItemsInventario(idAmbiente);
}

    public void cargarItemsInventario(int idAmbiente) {
        DefaultTableModel modelo = (DefaultTableModel) tb_itemInventario.getModel();
        modelo.setRowCount(0);

        InventarioAmbiente ia = new InventarioAmbiente();
        java.util.Iterator<InventarioAmbiente> lista = ia.listarPorAmbiente(idAmbiente);

        while (lista.hasNext()) {
            InventarioAmbiente item = lista.next();
            String estado = item.tieneStockBajo() ? "Stock bajo" : "Normal";
            modelo.addRow(new Object[]{
                item.getNombreArticulo(),
                item.getCantidad(),
                item.getCantidadMinima(),
                estado,
                ""
            });
        }
    }

